class TestJuAnticommutation:
    """Tests for J_u's commutation/anticommutation with Clifford generators."""

    def test_ju_anticommutation_pattern(self, anticomm_result):
        """J_u's relation to each gamma_i is unambiguous: each norm is
        either < 1e-14 (anticommutes/commutes) or > 0.1 (does not).
        No ambiguous cases.
        (acceptance test: test-ju-anticommutation)
        """
        for i, norm in enumerate(anticomm_result['anticommutator_norms']):
            assert norm < 1e-14 or norm > 0.1, (
                f"Ambiguous for gamma_{i+1}: |{{J_u, gamma}}| = {norm}")

    def test_ju_does_not_anticommute_with_all(self, anticomm_result):
        """J_u does NOT anticommute with all 9 gamma_i.

        This means J_u is NOT a '10th Clifford generator' extending Cl(9) to Cl(10).
        The Boyle branching S_{10}^+|_{Spin(9)} = S_9^C does not apply in the
        naive sense of J_u being an additional Clifford generator.
        (acceptance test: test-ju-anticommutation)
        """
        assert not anticomm_result['all_anticommute'], (
            "J_u anticommutes with all gamma_i -- unexpected!")

    def test_ju_commutes_with_gamma_1(self, anticomm_result):
        """J_u commutes with gamma_1 = 4*T_b[1] (diagonal traceless).

        This is because T_b[1] = (1/2)diag(I_8, -I_8) in the (x2, x3) basis,
        and J_u = diag(L_{e7}, L_{e7}) acts within each octonionic block.
        """
        assert anticomm_result['pattern'][0] == "commutes"

    def test_ju_squared_minus_identity(self, J_u):
        """Redundant cross-check: J_u^2 = -I (from Phase 28)."""
        J_sq = J_u @ J_u
        err = np.max(np.abs(J_sq + np.eye(16)))
        assert err < ATOL, f"J_u^2 != -I: max error = {err}"


