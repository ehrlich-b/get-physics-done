class TestJuDepth:
    """Tests for J_u's membership depth in the associative closure."""

    def test_ju_not_at_depth_0(self, closure_result):
        """J_u is not in the linear span of T_b (confirmed in Phase 28).
        Residual at depth 0 should be > 0.1.
        """
        assert closure_result['ju_residuals'][0] > 0.1, (
            f"J_u residual at depth 0 = {closure_result['ju_residuals'][0]} (too small)")

    def test_ju_not_at_depth_1(self, closure_result):
        """J_u is not in the Lie algebra (46-dim) either.
        Residual at depth 1 should be > 0.1.
        """
        assert closure_result['ju_residuals'][1] > 0.1, (
            f"J_u residual at depth 1 = {closure_result['ju_residuals'][1]} (too small)")

    def test_ju_depth_definite(self, closure_result):
        """J_u enters the closure at a definite depth d where residual
        drops from > 0.1 to < 1e-12.
        (acceptance test: test-ju-depth)
        """
        depth = closure_result['ju_depth']
        assert depth is not None, "J_u never entered closure"
        assert depth >= 2, f"J_u depth = {depth}, expected >= 2"
        # Verify sharp transition
        assert closure_result['ju_residuals'][depth - 1] > 0.1, (
            f"Residual at depth {depth-1} = {closure_result['ju_residuals'][depth-1]} (should be > 0.1)")
        assert closure_result['ju_residuals'][depth] < 1e-12, (
            f"Residual at depth {depth} = {closure_result['ju_residuals'][depth]} (should be < 1e-12)")

    def test_ju_in_final_closure(self, closure_result):
        """At the converged closure (dim 256), J_u membership residual < 1e-12."""
        final_residual = closure_result['ju_residuals'][-1]
        assert final_residual < 1e-12, (
            f"J_u not in final closure: residual = {final_residual}")

    def test_ju_depth_via_wrapper(self, T_matrices, J_u):
        """find_ju_depth returns the same depth as the full closure computation."""
        depth = find_ju_depth(T_matrices, J_u)
        assert depth is not None
        assert depth == 2


