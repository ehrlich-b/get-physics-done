class TestVolumeElement:
    """Tests for the Cl(9,0) volume element omega = gamma_1 * ... * gamma_9."""

    def test_volume_element_squared(self, volume_result):
        """omega^2 = I_{16} to machine precision.
        (acceptance test: test-volume-element)
        """
        assert volume_result['omega_squared_error'] < ATOL, (
            f"omega^2 error: {volume_result['omega_squared_error']}")

    def test_volume_element_eigenvalues(self, volume_result):
        """All eigenvalues are +1 (not -1 or mixed).

        For n=9 odd: omega = gamma_1...gamma_9 satisfies omega^2 = +I
        (since (-1)^{9*8/2} = (-1)^{36} = +1). On the irrep, omega = +I or -I.
        The +1 result identifies which simple factor of Cl(9,0) = M_16(R) + M_16(R)
        the representation uses: the P_+ = (1+omega)/2 factor.
        (acceptance test: test-volume-element)
        """
        assert volume_result['which_factor'] in ("+1", "-1"), (
            f"Mixed eigenvalues: {volume_result['eigenvalues']}")
        # The specific sign (+1) is a property of our convention.
        # Either +1 or -1 is valid; what matters is ALL same sign.

    def test_volume_element_commutes_with_generators(self, gamma_matrices, volume_result):
        """omega commutes with all gamma_i (since n=9 is odd).

        For odd n, the volume element is central in Cl(n): omega * gamma_i = gamma_i * omega.
        This is because moving gamma_i through gamma_1...gamma_9 requires passing through
        (n-1) = 8 other generators, picking up (-1)^8 = +1.
        """
        omega = volume_result['omega']
        for i, g in enumerate(gamma_matrices):
            comm = omega @ g - g @ omega
            comm_norm = np.linalg.norm(comm, 'fro')
            assert comm_norm < ATOL, (
                f"[omega, gamma_{i+1}] norm = {comm_norm} (should be 0)")


