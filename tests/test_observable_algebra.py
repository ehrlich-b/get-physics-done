class TestCrossValidation:
    """Cross-validation of Clifford structure."""

    def test_clifford_anticommutation_rescaled(self, gamma_matrices):
        """Verify {gamma_i, gamma_j} = 2*delta_{ij}*I for the rescaled generators.

        Redundant with Phase 28 but verifies the rescaling gamma_1 = 4*T_b[1],
        gamma_k = 2*T_b[k] is correct.
        """
        for i in range(9):
            for j in range(i, 9):
                ac = gamma_matrices[i] @ gamma_matrices[j] + gamma_matrices[j] @ gamma_matrices[i]
                if i == j:
                    err = np.max(np.abs(ac - 2.0 * np.eye(16)))
                    assert err < ATOL, (
                        f"{{gamma_{i+1}, gamma_{i+1}}} != 2I: err = {err}")
                else:
                    err = np.max(np.abs(ac))
                    assert err < ATOL, (
                        f"{{gamma_{i+1}, gamma_{j+1}}} != 0: err = {err}")

    def test_closure_basis_orthonormal(self, closure_result):
        """Verify the returned basis is approximately orthonormal."""
        basis = closure_result['basis']  # 256 x 256
        gram = basis.T @ basis
        err = np.max(np.abs(gram - np.eye(gram.shape[0])))
        assert err < 1e-10, (
            f"Basis not orthonormal: max |G - I| = {err}")


