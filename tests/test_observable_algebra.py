class TestGradeDecomposition:
    """Tests for Clifford grade decomposition."""

    def test_grade_decomp_identity(self, gamma_matrices):
        """I_{16} should be pure grade-0."""
        result = compute_grade_decomposition(np.eye(16), gamma_matrices)
        assert result['reconstruction_error'] < 1e-13
        assert result['dominant_grade'] == 0
        assert abs(result['coefficients'][frozenset()]) > 0.5
        # No other grades should contribute
        for k in range(1, 5):
            assert result['grade_norms'][k] < ATOL, (
                f"Grade {k} norm = {result['grade_norms'][k]} for identity")

    def test_grade_decomp_gamma(self, gamma_matrices):
        """Each gamma_i should be pure grade-1."""
        for i, g in enumerate(gamma_matrices):
            result = compute_grade_decomposition(g, gamma_matrices)
            assert result['reconstruction_error'] < 1e-13
            assert result['dominant_grade'] == 1, (
                f"gamma_{i+1}: dominant grade = {result['dominant_grade']}")
            for k in [0, 2, 3, 4]:
                assert result['grade_norms'][k] < ATOL, (
                    f"gamma_{i+1}: grade {k} norm = {result['grade_norms'][k]}")

    def test_grade_decomp_commutator(self, gamma_matrices):
        """[gamma_i, gamma_j] = 2 * gamma_i * gamma_j (for i != j) should be pure grade-2."""
        g0, g1 = gamma_matrices[0], gamma_matrices[1]
        comm = g0 @ g1 - g1 @ g0
        # comm = 2 * gamma_0 * gamma_1 (since {g0,g1}=0, so g0*g1 = -g1*g0, comm = 2*g0*g1)
        result = compute_grade_decomposition(comm, gamma_matrices)
        assert result['reconstruction_error'] < 1e-13
        assert result['dominant_grade'] == 2

    def test_grade_decomp_volume(self, gamma_matrices, volume_result):
        """omega = gamma_1*...*gamma_9 = +I, so it should decompose as grade-0 = 1."""
        # omega = +I on this irrep, so it IS the identity
        omega = volume_result['omega']
        result = compute_grade_decomposition(omega, gamma_matrices)
        assert result['reconstruction_error'] < 1e-13
        assert result['dominant_grade'] == 0

    def test_grade_decomp_ju(self, grade_result):
        """J_u grade decomposition: identify dominant grade, verify reconstruction.
        (acceptance test: test-ju-grade-decomp)
        """
        assert grade_result['reconstruction_error'] < 1e-13
        # J_u should have nonzero grades 2 and 3 (mixed grade)
        assert len(grade_result['nonzero_grades']) >= 2
        nonzero_k = [k for k, _ in grade_result['nonzero_grades']]
        assert 2 in nonzero_k, "Grade 2 should be nonzero for J_u"
        assert 3 in nonzero_k, "Grade 3 should be nonzero for J_u"

    def test_grade_norm_sum(self, J_u, grade_result, gamma_matrices):
        """Parseval-like relation: sum of squared coefficient norms = squared total norm / 16.

        Since the Clifford monomials are orthogonal under Tr(A^T B) / 16,
        and we expand M = sum c_S gamma_S, we have:
        Tr(M^T M) / 16 = sum c_S^2 * Tr(gamma_S^T gamma_S) / 16 = sum c_S^2
        (because Tr(gamma_S^T gamma_S)/16 = 1 for each monomial).

        So: ||M||_F^2 / 16 = sum c_S^2 = sum_k ||grade-k-coeffs||^2
        """
        # Sum of squared coefficient norms
        coeff_norm_sq = sum(c**2 for c in grade_result['coefficients'].values())
        # ||J_u||_F^2 / 16
        ju_norm_sq = np.linalg.norm(J_u, 'fro')**2 / 16.0
        rel_err = abs(coeff_norm_sq - ju_norm_sq) / ju_norm_sq
        assert rel_err < 1e-12, (
            f"Parseval mismatch: sum c^2 = {coeff_norm_sq}, ||J_u||^2/16 = {ju_norm_sq}")


