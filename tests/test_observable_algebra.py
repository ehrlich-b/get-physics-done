class TestAssociativeClosure:
    """Tests for the associative algebra generated by {T_b}."""

    def test_closure_depth_0_is_10(self, closure_result):
        """Dimension at depth 0 = 10 (the 10 generators span a 10-dim space)."""
        assert closure_result['dimensions'][0] == 10

    def test_closure_converges_to_256(self, closure_result):
        """Final dimension = 256 = dim M_16(R).

        This confirms the Cl(9,0) irreducible representation surjects onto
        End(R^16). Benchmark: Lawson-Michelsohn Table I.4.3, Cl(9,0) = M_16(R) + M_16(R),
        so the irrep on R^16 hits all of M_16(R).
        (acceptance test: test-closure-dim-256)
        """
        final_dim = closure_result['dimensions'][-1]
        assert final_dim == 256, f"Expected 256, got {final_dim}"

    def test_closure_monotonic(self, closure_result):
        """Dimensions are non-decreasing at each depth.
        (acceptance test: test-closure-growth)
        """
        dims = closure_result['dimensions']
        for i in range(1, len(dims)):
            assert dims[i] >= dims[i - 1], (
                f"Non-monotonic: depth {i-1}={dims[i-1]} -> depth {i}={dims[i]}")

    def test_closure_growth_sequence(self, closure_result):
        """Verify the dimension growth 10 -> 46 -> 130 -> 256.

        Depth 0: 10 generators.
        Depth 1: 10 + 36 (commutator algebra = spin(9)) = 46.
        Depth 2: 130 (grade 2+3 elements added).
        Depth 3: 256 (full M_16(R)).
        (acceptance test: test-closure-growth)
        """
        dims = closure_result['dimensions']
        assert dims[0] == 10
        assert dims[1] == 46
        assert dims[2] == 130
        assert dims[3] == 256

    def test_closure_contains_identity(self, T_matrices, closure_result):
        """I_{16} is in the closure (4*T_b[0] = I from trace element)."""
        I16 = np.eye(16)
        I_flat = I16.flatten()
        basis = closure_result['basis']
        coeffs, _, _, _ = np.linalg.lstsq(basis, I_flat, rcond=None)
        residual = np.linalg.norm(I_flat - basis @ coeffs)
        assert residual < 1e-12, f"Identity not in closure: residual = {residual}"


